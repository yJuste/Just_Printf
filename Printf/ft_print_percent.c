#include "ft_printf.h"

// --------------------------PROTOTYPE--------------------------
void	ft_print_percent(t_flags *flags, const char **format);
// -------------------------------------------------------------

void	ft_print_percent(t_flags *flags, const char **format)
{
	write(1, "%", 1);
	flags->count++;
	(*format)++;
}
